    public boolean verificaCampi(){
        String nome = jTextFieldNomeRistorante.getText();      
        String indirizzo = jTextFieldIndirizzo.getText();        
        String numeroCivico = jTextFieldCivico.getText();
        String comune = jTextFieldComune.getText();
        String provincia = jTextFieldProvincia.getText();
        String cap = jTextFieldCap.getText();
        String telefono = jTextFieldTelefono.getText();        
        String url = jTextFieldUrl.getText();
        String username = jTextFieldUsername.getText();
        String passwd = String.valueOf(jPasswordField1.getPassword());
        String passwdConf = String.valueOf(jPasswordField2.getPassword());
        
        if(nome.trim().equals("") || username.trim().equals("") || url.trim().equals("") || indirizzo.trim().equals("") || numeroCivico.trim().equals("") || passwd.trim().equals("") || passwdConf.trim().equals("") || comune.trim().equals("") || provincia.trim().equals("") || cap.trim().equals("") || telefono.trim().equals("") ){
            JOptionPane.showMessageDialog(null, "uno o pi√π campi sono vuoti");
            return false;
        }
        // ora controllo se le password coincidono
        else if(!passwd.equals(passwdConf)){
          JOptionPane.showMessageDialog(null, "la password non coincide");  
          return false;
        }
        
        //se invece √® tutto ok..
        else{
            return true;
        }
           
    }
    
   
